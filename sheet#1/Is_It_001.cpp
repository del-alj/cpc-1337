#include <iostream>

using namespace std;


int	main()
{
	int		n, m;
	char	s;

	cin >> n >> s >> m;
	((s == '>' && n > m) ||(s == '<' && n < m) || (s == '=' && n == m)) ? cout << "Right\n" :  cout << "Wrong\n";
	return (0);
}
